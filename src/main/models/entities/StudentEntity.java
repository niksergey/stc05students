package main.models.entities;

import javax.persistence.*;

@Entity
@Table(name = "student", schema = "public", catalog = "students")
public class StudentEntity {
    private int id;
    private String name;
    private Integer age;
    private StudyGroupEntity studyGroupEntity;

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @ManyToOne
    @JoinColumn(name="group_id")
    public StudyGroupEntity getStudyGroupEntity() {
        return studyGroupEntity;
    }

    public void setStudyGroupEntity(StudyGroupEntity studyGroupEntity) {
        this.studyGroupEntity = studyGroupEntity;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Basic
    @Column(name = "age")
    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        StudentEntity that = (StudentEntity) o;

        if (id != that.id) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;
        if (age != null ? !age.equals(that.age) : that.age != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        result = 31 * result + (age != null ? age.hashCode() : 0);
        return result;
    }
}
